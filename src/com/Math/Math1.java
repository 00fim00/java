package com.Math;

public class Math1 {
    public static void main(String[] args) {
        float num1 = 4.25f;
        int num2 = 5;

        System.out.println("Больше число из 2 чисел: " + Math.max(num1,num2));
        System.out.println("Меньше число из 2 чисел: " + Math.min(num1,num2));
    }
}
